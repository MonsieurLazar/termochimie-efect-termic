<h1><span class="highlight">Determinarea entalpiei de neutralizare</span></h1>
<h2><span class="highlight">Principiul lucrării:</span></h2>
<p class="indent_paragraph">
  Căldura de neutralizare a unui acid cu o bază reprezintă cantitatea de căldură
  degajată la formarea unui mol de apă (H<sub>2</sub>O) dintr-un ion gram de
  protoni hidratați H<sub>3</sub>O<sup>+</sup> și un ion gram de grupe hidroxil
  HO<sup>-</sup>. Pentru reacțiile dintre acizii și bazele tari, în soluții
  apoase diluate, entalpia de neutralizare are aceeași valoare, egală cu
  -13,7 kcal/mol, sau -57,3 kJ/mol la 18&deg;C (291 K) și 1 atm,
  indiferent de natura acidului și a bazei.
</p>
<p class="indent_paragraph">
  Așadar, la neutralizarea acizilor tari cu baze tari în soluție apoasă se produce
  practic reacția de formare a unui mol de apă:
</p>
<center class="formula">H<sup>+</sup><sub>aq</sub> + HO<sup>-</sup><sub>aq</sub> -> H<sub>2</sub>O    (1)</center>

<p class="indent_paragraph">
  Aceasta deoarece acizii și bazele tari, fiind electroliți tari, sunt total
  disociați și prin urmare reacția de neutralizare se poate scrie ca o reacție
  ionică.<br />Exemplu:
</p>

<center class="formula">
  (H<sup>+</sup><sub>(aq)</sub> + Cl<sup>-</sup><sub>(aq)</sub>) +
  (Na<sup>+</sup><sub>(aq)</sub> + OH<sup>-</sup><sub>(aq)</sub>) ->
  Na<sup>+</sup><sub>(aq)</sub> + Cl<sup>-</sup><sub>(aq)</sub> + H<sub>2</sub>O
  (2)
</center>
<p class="indent_paragraph">
  În care, reducând termenii asemenea rezultă ecuația (1). Când acidul, baza sau
  ambele sunt electroliți slabi, deci sunt parțial disociați în soluție, entalpia
  de neutralizare este mai mică, datorită energiei consumate pentru disocierea lor.
</p>

<br />
