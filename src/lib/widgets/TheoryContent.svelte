<h1><span class="highlight">Determinarea entalpiei de utilizare</span></h1>
<h2><span class="highlight">Principiul lucrarii:</span></h2>
<p class="indent_paragraph">
  Caldura de neutralizare a unui acid cu o baza reprezinta cantitatea de caldura
  degajata la formarea unui mol de apa (H<sub>2</sub>O) dintr-un ion gram de
  protoni hidratati H<sub>3</sub>O<sup>+</sup> si un ion gram de grupe hidroxil
  HO<sup>-</sup>. Pentru reactiile dintre acizii si bazele tari, in solutii
  apoase diluate, entalpia de neutralizare are aceeasi valoare, egala cu
  -13,7 kcal/mol, sau -57,3 kJ/mol la 18<sup>o</sup>C (291 K) si 1 atm,
  indiferent de natura acidului si a bazei.
</p>
<p class="indent_paragraph">
  Asadar, la neutralizarea acizilor tari cu baze tari in solutie apoasa se produce
  practic reactia de formare a unui mol de apa:
</p>
<center class="formula">H<sup>+</sup><sub>aq</sub> + HO<sup>-</sup><sub>aq</sub> -> H<sub>2</sub>O    (1)</center>

<p class="indent_paragraph">
  Aceasta deoarece acizii si bazele tari, fiind electroliti tari, sunt total
  disociati si prin urmare reactia de neutralizare se poate scrie ca o reactie
  ionica.<br />Exemplu:
</p>

<center class="formula">
  (H<sup>+</sup><sub>(aq)</sub> + Cl<sup>-</sup><sub>(aq)</sub>) +
  (Na<sup>+</sup><sub>(aq)</sub> + OH<sup>-</sup><sub>(aq)</sub>) ->
  Na<sup>+</sup><sub>(aq)</sub> + Cl<sup>-</sup><sub>(aq)</sub> + H<sub>2</sub>O
  (2)
</center>
<p class="indent_paragraph">
  In care, reducand termenii asemenea rezulta ecuatia (1). Cand acidul, baza sau
  ambele sunt electroliti slabi, deci sunt partial disociati in solutie, entalpia
  de neutralizare este mai mica, datorita energiei consumate pentru disocierea lor.
</p>

<br />
