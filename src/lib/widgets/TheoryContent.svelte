<center class="formula">a) HCl + NaOH -> H<sub>2</sub>O + NaCl    (3)</center>
<center class="formula">b) H<sub>2</sub>SO<sub>4</sub> + 2NaOH -> 2H<sub>2</sub>O + Na<sub>2</sub>SO<sub>4</sub>    (4)</center>
<center class="formula">c) HCl + NH<sub>4</sub>OH -> H<sub>2</sub>O + NH<sub>4</sub>Cl    (5)</center>

<br />
<h2><span class="highlight">Aparatura si substante</span></h2>
<p class="indent_paragraph"><b><i>Aparatura:</i></b> calorimetru, cilindri gradati, termometru</p>
<p class="indent_paragraph"><b><i>Substante:</i></b> solutii de HCl 1N, H2SO4 1N, NaOH 0,5N si NH4OH 0,5N</p>
<p class="indent_paragraph">
  Aparatul cu ajutorul caruia se masoara efectele termice ale proceselor chimice si
  fizice in conditii adiabatice, se numeste calorimetru. In laboratoarele de chimie
  generala se va folosi drept sistem adiabat un pahar Berzelius aflat intr-un vas
  cilindric de protectie din polistiren, care asigura izolarea termica.
</p>

<br />
<h2><span class="highlight">Modul de lucru</span></h2>
<ul>
  <li>
    <p class="indent_paragraph">
      se introduc in paharul de sticla al sistemului calorimetric 25 mL solutie de
      HCl 1N si se citeste, dupa aproximativ 3 minute, temperatura acestei solutii
      (t<sub>i</sub>) cu ajutorul unui termometru;
    </p>
  </li>
  <li>
    <p class="indent_paragraph">
      se adauga 50 mL NaOH 0,5 N, la aceeasi temperatura, peste solutia aflata in
      calorimetru;
    </p>
  </li>
  <li>
    <p class="indent_paragraph">
      din momentul amestecarii se urmareste variatia temperaturii pana cand ea devine
      constanta si se noteaza valoarea ei (t<sub>f</sub>).
    </p>
  </li>
</ul>

