<h1>Teorie</h1>
</div>

<!-- Paragraf introductiv -->
<p class = "indent_paragraph">
    Căldura de reacţie este efectul termic ce însoţeşte o reacţie chimică şi reprezintă
cantitatea de căldura absorbită sau degajată în timpul unei reacţii chimice. Ea depinde numai de
starea iniţială şi starea finală a sistemului chimic, nu şi de drumul parcurs de sistem în timpul
evoluţiei sale.
Căldura de reacţie se determina în majoritatea cazurilor în condiţii de presiune constantă,
Q<sub>p</sub> şi mai rar în condiţii de volum constant, Q<sub>v</sub>, la o anumită temperatură. 
Căldurile de reacţie la presiune constantă Q<sub>p</sub> = ∆H sunt chiar entalpiile de reacţie. Procesele
chimice în care sistemul reactant cedează căldura mediului exterior se numesc <span class = "highlight">
reacţii exoterme ∆H &lt; O </span>, iar cel în care sistemul absoarbe căldură din mediul exterior 
<span class = "highlight"> ∆H &gt; O se numesc reacţii endoterme </span>.
</p>

<!-- Descriere lucrarii -->
 <br>
<h1><span class = "highlight"> Determinarea entalpiei de utilizare </span></h1>
<h2><span class = "highlight"> Principiul lucrării: </span></h2>
<p class = "indent_paragraph">
Căldura de neutralizare a unui acid cu o bază reprezintă cantitatea de căldură degajată la
formarea unui mol de apă (H<sub>2</sub>O) dintr-un ion gram de protoni hidrataţi H<sub>3</sub>O<sup>+</sup>
şi un ion gram de grupe hidroxil HO<sup>−</sup>. Pentru reacţiile dintre acizii şi bazele tari, 
în soluţii apoase diluate, entalpia de neutralizare are aceeaşi valoare, egală cu -13,7 kcal/mol, 
sau – 57,3kJ/mol la 18<sup>o</sup>C (291 K) şi 1 atm, indiferent de natura acidului şi a bazei.
</p>
<p class = "indent_paragraph">Aşadar, la neutralizarea acizilor tari cu baze tari în soluţie apoasă se produce practic
reacţia de formare a unui mol de apă:</p>
<center class = "formula">H<sup>+</sup><sub>aq</sub> + HO<sup>-</sup><sub>aq</sub> → H<sub>2</sub>O&emsp;&emsp;(1)</center>

<!--Scriere reactie ionica -->
<p class = "indent_paragraph">
    Aceasta deoarece acizii şi bazele tari, fiind electroliţi tari, sunt total disociaţi şi prin urmare recţia
de neutralizare se poate scrie ca o reacţie ionică.<br>
    Exemplu:
</p>

<center class = "formula">
    (H<sup>+</sup><sub>(aq)</sub> + Cl<sup>-</sup><sub>(aq)</sub>) +
    (Na<sup>+</sup><sub>(aq)</sub> + OH <sup>-</sup><sub>(aq)</sub>) →
    Na<sup>+</sup><sub>(aq)</sub> + Cl<sup>-</sup><sub>(aq)</sub> + H<sub>2</sub>O
    &emsp;&emsp;(2)
</center>
<p class = "indent_paragraph">
    în care, reducând termenii asemenea rezultă ecuaţia (1).
Când acidul, baza sau ambele sunt electroliţi slabi, deci sunt parţial disociaţi în soluţie,
entalpia de neutralizare este mai mică, datorită energiei consumate pentru disocierea lor.
</p>

<!-- Scopul lucrarii -->
<br>
<h2><span class = "highlight">Scopul lucrării:</span></h2>
<p class = "indent_paragraph">Determinăm entalpia de neutralizare pentru următoarele reacţii:</p>
<center class = "formula">
    a) HCl + NaOH → NaCl + H<sub>2</sub>O &emsp;&emsp;(3)
</center>
<center class = "formula">
    b) H<sub>2</sub>SO<sub>4</sub> + 2NaOH → 
    Na<sub>2</sub>SO<sub>4</sub> + 2H<sub>2</sub>O &emsp;&emsp;(4)
</center>
<center class = "formula">
    c) HCl + NH<sub>4</sub>OH → 
    NH<sub>4</sub>Cl + H<sub>2</sub>O &emsp;&emsp;(5)
</center>

<!-- Aparatura si substante-->
<br>
<h2><span class = "highlight">Aparatură şi substanţe</span></h2>
<p class = "indent_paragraph"><b><i>Aparatura: </i></b> calorimetru, cilindri gradaţi, termometru</p>
<p class = "indent_paragraph"><b><i>Substanţe: </i></b> soluţii de HCl 1N, H2SO4 1N, NaOH 0,5N şi NH4OH 0,5N</p>
<p class = "indent_paragraph">
    Aparatul cu ajutorul căruia se măsoară efectele termice ale proceselor chimice şi fizice în
condiţii adiabatice, se numeşte calorimetru. În laboratoarele de chimie generală se va folosi drept
sistem adiabat un pahar Berzelius aflat într-un vas cilindric de protecţie din polistiren, care asigură
izolarea temică. În paharul de sticlă se introduc soluţiile şi termometrul, iar agitarea mecanică se
realizează mişcând cu grijă sistemul pentru uniformizarea temperaturii.
</p>

<!-- Modul in care e realizata lucrarea -->
<br>
<h2><span class = "highlight"> Modul de lucru </span></h2>
<ul>
    <li>
        <p class = "indent_paragraph">
            se introduc în paharul de sticlă al sistemului calorimetric 25 mL soluţie de HCl 1N şi se
            citeşte, după aproximativ 3 minute, temperatura acestei soluţii (t<sub>i</sub>) cu ajutorul unui termometru;
        </p>
    </li>
    <li>
        <p class = "indent_paragraph">
            se adaugă 50 mL NaOH 0,5 N, la aceeaşi temperatură, peste soluţia aflată în calorimetru;
        </p>
    </li>
    <li>
        <p class = "indent_paragraph">
             din momentul amestecării se urmăreşte variaţia temperaturii până când ea devine constantă
            şi se notează valoarea ei (t<sub>f</sub>).
        </p>
    </li>
</ul>
